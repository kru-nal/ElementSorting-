package com.sorting;
import java.util.LinkedList;
public class LinkedListDemo {
public static void main(String[] args) {
	
	
	LinkedList <String> list = new LinkedList<String>();
	
	list.add("India");
	list.add("Russia");
	
	list.add("Israel");
	
	for (String A:list) {
		System.out.println(A);
	}
}
}
